package com.xmtq.lottery.bean;

public class Odds {
	/**
	 * 玩法结果
	 */
	private String result;
	
	/**
	 * 玩法赔率
	 */
	private String odds;
	
	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getOdds() {
		return odds;
	}

	public void setOdds(String odds) {
		this.odds = odds;
	}
}
